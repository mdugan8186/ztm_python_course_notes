# ==== parameters and arguments ====

# parameters
# they are variables for your code block when we define a function. you can have as many parameters as you want 
def say_hello(name, emoji):
  print(f'Hello {name} {emoji}')


# arguments
# these are the actual values we provide to a function when we call (or invoke it)
say_hello('Dugan', ';)')

say_hello('Dan', ':)')

say_hello('Emily', ':)')
