# Numbers
# Python will automatically convert to a point or to a float

# int - integer (whole numbers)
# 3 4 5
 
# print() will print what's in the brackets 
# type() wil show you what type of data type something is

print(type(2 + 4)) # 6 (int)
print(2 + 4)
print(2 - 4)
print(2 * 4)
print(type(0))

#  ** (the power of)
print(2 ** 2) #(2 to the power of 2)

# // (double divide)(floor division, rounds the result down to the nearest whole number)
print(2 // 4) #(0) returns an integer rounded down
print(5 // 4) #(1) 
print(15 // 2) #(7)

# % modulo (remainder)
print(5 % 4) #(1) returns the remainder

# float - floating point number (a number with a decimal point)
# floats take up much more memory because it uses binary for the numbers in front of the decimal and another binary for the number behind the decimal

print(type(2 / 4)) # 0.5 (float)
print(type(0.00001))
print(type(5.00001))

print(9.9 + 1.1)
print(type(9.9 + 1.1))

