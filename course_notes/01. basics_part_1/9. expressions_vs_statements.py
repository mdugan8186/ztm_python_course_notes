# ==== expression vs statement ====

iq = 100
user_age = iq / 5

# an expression is the iq / 5 part of the code. it's a piece of code that produces a value

# a statement is an entire line of code that performs an action
# user_age = iq / 5 is a statement.
# the action is that we're calculating 100(iq) divided by 5 and then assigning that value to the variable user_age