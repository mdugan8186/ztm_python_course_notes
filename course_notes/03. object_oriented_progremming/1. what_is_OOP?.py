# ==== what is OOP? ====

# object oriented programming

# everything in python is an object
print(type(None))  # <class 'NoneType'>
print(type(True))  # <class 'bool'>
print(type(5))  # <class 'int'>
print(type(5.5))  # <class 'float'>
print(type('hi'))  # <class 'str'>
print(type([]))  # <class 'list'>
print(type(()))  # <class 'list'>
print(type({}))  # <class 'dict'>

# everything has the class keyword in front of it denoting they are objects
# we are able to use different methods on our objects to perform some actions on them (ex. 'hi'.capitalize()) and attributes we can access with dot the dot method

# OOP allows us to go beyond what python gives us and lets us create our own objects with their own attributes and methods

# OOP is called a paradigm, a way for us to think about or code and structure our code in way that is easier to maintain, extend, and write. your code will be more organized
