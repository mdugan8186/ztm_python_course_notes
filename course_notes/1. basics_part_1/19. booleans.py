# ==== booleans ====

# bool
# only has 2 options, True or False
# like saying 0 and 1 in programming

name = 'Dugan'
is_cool = False
print(name, is_cool)

is_cool = True

print(name, is_cool)


# a zero (0) will be False and any number 1 and over will be True
print(bool(0)) # False
print(bool(1)) # True
print(bool(2)) # True


# convert a string into a boolean
print(bool('True')) # True