# ==== Data types ====

# == Fundamental Data Types ==

# int - integer (all numbers w/ out a decimal)

# float - floating point number (decimals)

# complex

# str - strings (all letters, or anything between quotation marks)

# bool - True or False

# list - an ordered sequence of objects that can be of any data type

# tuple - stores multiple items in a single variable that are immutable

# set - sets are unordered collections of unique objects
 
# dict - an unordered key value pair, (since python 3.7 dictionaries are ordered)

# Classes -> create your own data type (custom types)


