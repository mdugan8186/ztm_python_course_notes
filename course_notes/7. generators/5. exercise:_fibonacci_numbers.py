# ==== exercise: fibonacci numbers ====

# this may come up as an interview question


# using range
def fib(number):
    a = 0
    b = 1
    for i in range(number):
        yield a
        temp = a
        a = b
        b = temp + b


for x in fib(10):
    print(x)

print(list(fib(10)))


# using a list
def fib2(number):
    a = 0
    b = 1
    result = []
    for i in range(number):
        result.append(a)
        temp = a
        a = b
        b = temp + b
    return result


print(fib2(10))
