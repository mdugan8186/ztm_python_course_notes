# ==== setting up flask ====

# Create a project folder and a .venv folder within:

# macOS/Linux
# mkdir myproject
# cd myproject
# python3 -m venv .venv
# == the second .venv is where the name of the folder is if you don't want to create a venv folder ==
# example of this:
# python3 -m venv 01.\ building_our_first_server


# Activate the environment
# Before you work on your project, activate the corresponding environment:

# macOS/Linux
# . .venv/bin/activate
# == the .venv is where the name of the folder is if you're working with a folder with a different name ==
# an example of this:
# . 01.\ building_our_first_server/bin/activate

# Your shell prompt will change to show the name of the activated environment.


# Install Flask
# Within the activated environment, use the following command to install Flask:

# pip install Flask

# Flask is now installed. Check out the Quickstart or go to the Documentation Overview.


# to deactivate the virtual environment
# deactivate
# or
# close the terminal
